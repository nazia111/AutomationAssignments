package com.creatio.crm.language.basics;

public class Datatype {

	public static void main(String[] args)
	{
		//Datatype used to store simple integer
		int a = 12;
		System.out.println(a);
		
		//datatype to store decimal number
		float p = 92.3f;
		System.out.println(p);
		
		//datatype used to store large number of data
		long m = 95532349792L;
		System.out.println(m);
		
		/* datatype used to store single character
		 * the value is given within single quotes here.
		 */
		char g = 'A';
		System.out.println(g);
		
		//datatype used to store set of characters
		String name = "Nazia";
		System.out.println(name);
		
		//datatype used to store large decimal number 
		double t= 35.655;
		System.out.println(t);
	}
	
}
